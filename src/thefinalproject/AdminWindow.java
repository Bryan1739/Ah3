    public void refeshTable(){
        //pito
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Object[] row;
        for(int i = 0; i < vendedorlist.size(); i++){
            row = new Object[5];
            row[0] = vendedorlist.get(i).getCodigo();
            row[1] = vendedorlist.get(i).getNombre();
            row[2] = vendedorlist.get(i).getCaja();
            row[3] = vendedorlist.get(i).getVentas();
            row[4] = vendedorlist.get(i).getGenero();
            model.addRow(row);
            
        }
    }
