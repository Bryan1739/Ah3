public void refenshBotones(){
    int rowIndex = jTable1.getSelectedRow();       
    if (rowIndex >= 0 ){
    jButtonUpdate.setEnabled(true);
    jButtonEliminao.setEnabled(true);
    vendedor = vendedorlist.get(rowIndex);
    }
    else {
    jButtonUpdate.setEnabled(false);
    jButtonEliminao.setEnabled(false);
    }
    
}

